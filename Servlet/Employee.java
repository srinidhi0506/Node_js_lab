package coma4;

public class Employee {
        private String empname;
        private int eid;
        private int esal;
		public String getEmpname() {
			return empname;
		}
		public void setEmpname(String empname) {
			this.empname = empname;
		}
		public int getEid() {
			return eid;
		}
		public void setEid(int eid) {
			this.eid = eid;
		}
		public int getEsal() {
			return esal;
		}
		public void setEsal(int esal) {
			this.esal = esal;
		}
        
        
}
